package Model;

/**
 *
 * @author felip
 */
public class Produto {

    private int id;
    private String name;
    private String desc;
    private float price;
    private int quant;

    public Produto(String name, String desc, float price, int quant) {
        this.name = name;
        this.desc = desc;
        this.price = price;
        this.quant = quant;
    }
    
    public Produto(){
        
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public int getQuant() {
        return quant;
    }

    public void setQuant(int quant) {
        this.quant = quant;
    }

}
